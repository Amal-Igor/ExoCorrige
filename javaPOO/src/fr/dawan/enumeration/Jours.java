package fr.dawan.enumeration;

/*
 * Une enumera est une liste de valeurs constante
 * Une énumeration s'utilise lorsque l'ont veut créer un type possédant plusieurs valeurs fixes
 * Comme les jours de la semaine par exemple 
 * Permet d'enrichir les types que nous avons déjà à disposition
 */

public enum Jours {
	LUNDI,
	MARDI,
	MERCREDI,
	JEUDI,
	VENDREDI, 
	SAMEDI,
	DIMANCHE, 
}
